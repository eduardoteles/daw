class Person
{
	string name;
	int age;
	string gender;

	public void Person()
	{
		this.Name=Name;
        this.Age = Age;
        this.Gender = Gender;
    }

	public global::System.String Name { get => name; set => name = value; }
	public global::System.Int32 Age { get => age; set => age = value; }
	public global::System.String Gender { get => gender; set => gender = value; }

}
